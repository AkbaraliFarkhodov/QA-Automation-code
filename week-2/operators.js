//Arithmetic Operator

// 1. Addition

const number = 5;

console.log(number + 3);

// 2. Substraction

console.log(9 - 4);

// 3. Multiplation

console.log(2 * 4);

//4. Division

console.log(8 / 4);

//5. Exponention

console.log(2 ** 3);

//6. Modulus (Remainder) operator

console.log(8 % 2);
console.log(10 % 7);

//7. Increment operator

let a = 5;

a++;

console.log(a);

//8. Decrement operator

let b = 11;

b--;
b--;
b--;
b--;

// Comparison Operators - Will give us a boolean data types

//1. Strict equal

console.log(5 === 5);
console.log(5 === 8);

//2. Not strict equal

console.log(5 !== 5);
console.log(5 !== 8);

//3. Greater

console.log(20 > 12);

//4. Less than

console.log(30 < 20);

//5. Greater than or equal

console.log(8 >= 4);
console.log(8 >= 8);

//6. Less than or equal

console.log(10 <= 10);
console.log(12 <= 20);

console.log("5" === "5");
console.log(5 == 5);

