// 1.  IF // Executes a block of code if the condition is true.

let age = 24;

if (age > 18) {
  // console log needs to be in side curly brackets
  console.log("Hey, you are at an age you can go to college");
}
console.log(age);

// 2. ELSE IF // Specifies a new condition if the previous if condition is false.

let studentScore = 75;

if (studentScore > 90) {
  console.log("Grade A");
} else if (studentScore > 75) {
  console.log("Grade B");
}
